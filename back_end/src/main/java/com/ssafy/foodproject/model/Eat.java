package com.ssafy.foodproject.model;

public class Eat {
	int code;
	int count;
	public Eat(int code, int count) {
		super();
		this.code = code;
		this.count = count;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
